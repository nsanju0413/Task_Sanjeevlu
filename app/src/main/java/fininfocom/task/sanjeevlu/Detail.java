package fininfocom.task.sanjeevlu;

public class Detail {
    public String name,city;
    int age;
    public Detail()
    {
    }
    public Detail(int age, String city, String name) {
        this.name = name;
        this.age = age;
        this.city = city;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }


}
